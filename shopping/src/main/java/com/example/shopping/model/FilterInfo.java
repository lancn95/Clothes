package com.example.shopping.model;

public class FilterInfo {
	public String from;
	public String to;

	public FilterInfo() {
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

}
